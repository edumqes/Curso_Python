#Tipo bool (true or false)

'''
Operadores relacionais (comparação)
Igual ==
Maior que >
Menor que <
Maior ou igual >=
Menor ou igual <=
Diferente !=
'''

print(10 == 10)   #True
print(10 != 10)   #False
print(type(0 == 0))   #Type class bool

print(10.0 == 10)   #True float = int
print('10' == 10)   #False str != int