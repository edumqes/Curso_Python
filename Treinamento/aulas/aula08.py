#Operadores aritméticos

'''
Soma (x + y)
Subtração (x - y)
Multiplicação (x * y)
Divisão (x / y)
Divisão inteira (x // y)
Potência (x ** y)
Mod - resto da divisão (x % y)
'''

x = 12
y = 3

print('Soma:',x + y)
print('Subtração:',x - y)
print('Multiplicação:',x * y)
print('Divisão:',x / y)
print('Divisão inteira:',x // y)
print('Potência:',x ** y)
print('Mod:',x % y)